def test_expected_outputs_file_exists():
    """The expected_outputs.json file must exist (generated by generate_data.py)."""
    outputs_file = DATA_DIR / "expected_outputs.json"
    if not outputs_file.exists():
        pytest.skip("expected_outputs.json not found — run: python scripts/generate_data.py")

    outputs = json.loads(outputs_file.read_text())
    assert len(outputs) == 10, f"Expected 10 questions, got {len(outputs)}"

    for output in outputs:
        assert "question_id" in output
        assert "question" in output
        assert "key_facts" in output
        assert output["question_id"].startswith("Q")


def test_q10_response_requires_knowledge_gaps():
    """
    Q10 (what don't we know) is the gap identification question.
    A correct response must have non-empty knowledge_gaps.

    A system that says 'I don't know X because Y data is missing' scores
    higher than one that fabricates or returns an error.
    """
    # Simulate a correct Q10 response
    response = ResearchResponse(
        question_id="Q10",
        question="What don't we know about today's incident?",
        answer="Several gaps exist in our understanding of today's incident.",
        confidence=0.65,  # Q10 should have lower confidence by design
        iterations_used=3,
        sources=[
            Source(type=DataSource.MELT, document_id="melt-log-payment-svc-gap", relevance_score=0.6, excerpt="No heap dump captured")
        ],
        claims=[
            Claim(claim="No heap dump was captured", confidence=0.90, source_id="melt-log-payment-svc-gap")
        ],
        knowledge_gaps=[
            "Exact root cause of memory leak unknown — no heap dump captured",
            "Distributed traces incomplete — gateway-svc trace correlation missing",
            "Customer transaction impact count unavailable",
        ],
        latency_ms=8000,
        token_usage={"input": 2000, "output": 600},
        cost_usd=0.015,
    )

    assert len(response.knowledge_gaps) > 0, "Q10 must identify at least one knowledge gap"
    assert response.confidence < 1.0, "Q10 should never be 100% confident"


