@skip_without_integration
@pytest.mark.asyncio
async def test_q1_payment_service_responsibilities():
    """Q1: What is the payment service responsible for? (Low, OpenSearch)"""
    from src.agents.query_understanding import QueryUnderstandingAgent
    from src.agents.response_generator import ResponseGenerator
    from src.agents.retrieval_orchestrator import RetrievalOrchestrator
    from src.retrieval.melt_retriever import MELTRetriever
    from src.retrieval.neo4j_retriever import Neo4jRetriever, make_neo4j_driver
    from src.retrieval.opensearch_retriever import OpenSearchRetriever, make_opensearch_client

    api_key = os.environ["ANTHROPIC_API_KEY"]
    os_client = await make_opensearch_client()
    neo4j_driver = await make_neo4j_driver("bolt://localhost:7687", "neo4j", "bugraidpassword")
    melt = MELTRetriever(DATA_DIR / "melt_telemetry.json")
    melt.load()

    orchestrator = RetrievalOrchestrator(
        OpenSearchRetriever(os_client),
        Neo4jRetriever(neo4j_driver),
        melt,
    )
    query_agent = QueryUnderstandingAgent(api_key)
    generator = ResponseGenerator(api_key)

    query = "What is the payment service responsible for?"
    plan = await query_agent.parse(query)
    evidence, _ = await orchestrator.retrieve(plan)
    response = await generator.generate(query, evidence, plan, "Q1")

    # Must have sources and claims
    assert len(response.sources) > 0
    assert len(response.claims) > 0
    # Every claim must have a source
    source_ids = {s.document_id for s in response.sources}
    for claim in response.claims:
        assert claim.source_id in source_ids or claim.source_id == "unknown"

    await os_client.close()
    await neo4j_driver.close()


@skip_without_integration
@pytest.mark.asyncio
async def test_full_pipeline_streaming():
    """Verify that streaming works and first token arrives within 10s."""
    import time

    from src.agents.query_understanding import QueryUnderstandingAgent
    from src.agents.response_generator import ResponseGenerator
    from src.agents.retrieval_orchestrator import RetrievalOrchestrator
    from src.retrieval.melt_retriever import MELTRetriever
    from src.retrieval.neo4j_retriever import Neo4jRetriever, make_neo4j_driver
    from src.retrieval.opensearch_retriever import OpenSearchRetriever, make_opensearch_client

    api_key = os.environ["ANTHROPIC_API_KEY"]
    os_client = await make_opensearch_client()
    neo4j_driver = await make_neo4j_driver("bolt://localhost:7687", "neo4j", "bugraidpassword")
    melt = MELTRetriever(DATA_DIR / "melt_telemetry.json")
    melt.load()

    orchestrator = RetrievalOrchestrator(
        OpenSearchRetriever(os_client),
        Neo4jRetriever(neo4j_driver),
        melt,
    )
    query_agent = QueryUnderstandingAgent(api_key)
    generator = ResponseGenerator(api_key)

    query = "Is payment-svc healthy right now?"
    plan = await query_agent.parse(query)
    evidence, _ = await orchestrator.retrieve(plan)

    start = time.time()
    first_token_time = None
    chunks = []

    async for chunk in generator.generate_streaming(query, evidence, plan, "Q5"):
        if first_token_time is None and '"type": "token"' in chunk:
            first_token_time = time.time() - start
        chunks.append(chunk)

    assert first_token_time is not None, "No token chunks were received"
    assert first_token_time < 10, f"First token took {first_token_time:.1f}s (limit: 10s)"
    assert any('"type": "done"' in c for c in chunks), "No done event received"

    await os_client.close()
    await neo4j_driver.close()
