def test_response_schema_is_complete():
    """Every response must have all required fields."""
    response = ResearchResponse(
        question_id="Q1",
        question="What is the payment service responsible for?",
        answer="payment-svc handles payment processing [INC-2024-0487].",
        confidence=0.92,
        iterations_used=1,
        sources=[
            Source(
                type=DataSource.OPENSEARCH,
                document_id="INC-2024-0487",
                relevance_score=0.87,
                excerpt="payment-svc is responsible for...",
            )
        ],
        claims=[
            Claim(
                claim="payment-svc handles payment processing",
                confidence=0.95,
                source_id="INC-2024-0487",
            )
        ],
        knowledge_gaps=[],
        latency_ms=4821,
        token_usage={"input": 1240, "output": 380},
        cost_usd=0.0042,
    )

    # Validate all fields are present and typed correctly
    assert response.question_id == "Q1"
    assert 0 <= response.confidence <= 1
    assert response.iterations_used >= 1
    assert isinstance(response.sources, list)
    assert isinstance(response.claims, list)
    assert isinstance(response.knowledge_gaps, list)
    assert response.latency_ms >= 0
    assert response.cost_usd >= 0


